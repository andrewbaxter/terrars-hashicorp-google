pub struct BuildDataServiceAccount {
    pub tf_id: String,
    #[doc= ""]
    pub account_id: PrimField<String>,
}

impl BuildDataServiceAccount {
    pub fn build(self, stack: &mut Stack) -> DataServiceAccount {
        let out = DataServiceAccount(Rc::new(DataServiceAccount_ {
            shared: stack.shared.clone(),
            tf_id: self.tf_id,
            data: RefCell::new(DataServiceAccountData {
                depends_on: core::default::Default::default(),
                provider: None,
                for_each: None,
                account_id: self.account_id,
                id: core::default::Default::default(),
                project: core::default::Default::default(),
            }),
        }));
        stack.add_datasource(out.0.clone());
        out
    }
}

